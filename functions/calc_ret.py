def add(num1,num2):
    addition = num1 + num2
    return addition

def sub(num1,num2):
    subtraction = num1 - num2
    return subtraction

def mul(num1,num2):
    multiply = num1 * num2
    return multiply


print("The sum of two numbers",add(5,10))
print("The subtraction of two numbers",sub(10,2))

    